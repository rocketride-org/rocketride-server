"""
Hybrid search benchmark: BM25 + TF-IDF vector similarity with Reciprocal Rank Fusion.

Compares three retrieval modes across chunking frameworks:
  - BM25 only (inverted index with term frequency scoring)
  - Vector only (TF-IDF sparse vectors + cosine similarity)
  - Hybrid (Reciprocal Rank Fusion combining both retrievers)

Since real embedding models are too heavy for benchmarking, vector search is
simulated using sklearn TF-IDF vectors with cosine similarity — a reasonable
proxy for semantic similarity on keyword-rich documents.

RRF formula: score(d) = sum(1 / (k + rank_i(d))) for each retriever, k=60

Queries are generated by extracting 3 distinctive words from each document's
first sentence.

Usage:
    pip install -r benchmarks/requirements.txt
    python benchmarks/bench_hybrid_search.py <docs_directory>
"""

import gc
import math
import os
import re
import sys
import time
from collections import defaultdict

import psutil
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics.pairwise import cosine_similarity

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

RRF_K = 60
TOP_K = 10


def get_mem_mb():
    """Return current process RSS in MB."""
    return psutil.Process(os.getpid()).memory_info().rss / (1024 * 1024)


# ---------------------------------------------------------------------------
# Document loading
# ---------------------------------------------------------------------------


def load_docs(root_dir):
    """Load all text files from directory."""
    docs = []
    for dirpath, dirnames, filenames in os.walk(root_dir):
        dirnames[:] = [d for d in dirnames if d != '.git']
        for fname in sorted(filenames):
            fpath = os.path.join(dirpath, fname)
            try:
                with open(fpath, 'r', encoding='utf-8', errors='replace') as f:
                    content = f.read()
                if content.strip():
                    docs.append({'content': content, 'path': fpath, 'id': len(docs)})
            except OSError:
                continue
    return docs


# ---------------------------------------------------------------------------
# Query generation
# ---------------------------------------------------------------------------


def generate_queries(docs):
    """Generate keyword queries from each doc's first sentence.

    Picks the 3 longest words (>=4 chars) from the first sentence that
    has enough words — these act as distinctive terms for retrieval.
    """
    queries = []
    for doc in docs:
        sentences = re.split(r'(?<=[.!?])\s+', doc['content'])
        for sent in sentences[:3]:
            words = re.findall(r'\w{4,}', sent)
            if len(words) >= 3:
                query_words = sorted(words, key=len, reverse=True)[:3]
                queries.append({'query': ' '.join(query_words), 'doc_id': doc['id']})
                break
    return queries


# ---------------------------------------------------------------------------
# BM25 index
# ---------------------------------------------------------------------------


class BM25Index:
    """BM25 inverted index with term frequency scoring."""

    def __init__(self, k1=1.5, b=0.75):
        self.k1 = k1
        self.b = b
        self.index = defaultdict(list)  # word -> [(chunk_idx, tf)]
        self.doc_lens = []
        self.avg_dl = 0.0
        self.n_docs = 0
        self.idf = {}

    def build(self, chunks):
        word_pattern = re.compile(r'\w{2,}')
        tf_per_chunk = []

        for i, chunk in enumerate(chunks):
            words = word_pattern.findall(chunk['text'].lower())
            self.doc_lens.append(len(words))
            tf = defaultdict(int)
            for w in words:
                tf[w] += 1
            tf_per_chunk.append(tf)

        self.n_docs = len(chunks)
        self.avg_dl = sum(self.doc_lens) / max(1, self.n_docs)

        # Build posting lists
        df = defaultdict(int)
        for i, tf in enumerate(tf_per_chunk):
            for w, count in tf.items():
                self.index[w].append((i, count))
                df[w] += 1

        # IDF: log((N - df + 0.5) / (df + 0.5) + 1)
        for w, d in df.items():
            self.idf[w] = math.log((self.n_docs - d + 0.5) / (d + 0.5) + 1.0)

    def search(self, query, top_k=TOP_K):
        words = re.findall(r'\w{2,}', query.lower())
        if not words:
            return []

        scores = defaultdict(float)
        for w in words:
            w_idf = self.idf.get(w, 0.0)
            for chunk_idx, tf in self.index.get(w, []):
                dl = self.doc_lens[chunk_idx]
                # BM25 score
                numerator = tf * (self.k1 + 1)
                denominator = tf + self.k1 * (1 - self.b + self.b * dl / self.avg_dl)
                scores[chunk_idx] += w_idf * numerator / denominator

        ranked = sorted(scores.keys(), key=lambda c: (-scores[c], c))
        return ranked[:top_k]


# ---------------------------------------------------------------------------
# TF-IDF vector index
# ---------------------------------------------------------------------------


class VectorIndex:
    """TF-IDF sparse vector index using sklearn."""

    def __init__(self):
        self.vectorizer = TfidfVectorizer(
            max_features=50000,
            sublinear_tf=True,
            dtype='float32',
        )
        self.matrix = None

    def build(self, chunks):
        texts = [c['text'] for c in chunks]
        self.matrix = self.vectorizer.fit_transform(texts)

    def search(self, query, top_k=TOP_K):
        if self.matrix is None or self.matrix.shape[0] == 0:
            return []
        q_vec = self.vectorizer.transform([query])
        sims = cosine_similarity(q_vec, self.matrix).flatten()
        # Get top-k indices sorted by similarity descending
        top_indices = sims.argsort()[::-1][:top_k]
        return [int(i) for i in top_indices if sims[i] > 0]


# ---------------------------------------------------------------------------
# Reciprocal Rank Fusion
# ---------------------------------------------------------------------------


def reciprocal_rank_fusion(rankings, k=RRF_K):
    """Combine multiple ranked lists using RRF.

    score(d) = sum(1 / (k + rank_i(d))) for each retriever i
    """
    scores = defaultdict(float)
    for ranking in rankings:
        for rank, doc_idx in enumerate(ranking, start=1):
            scores[doc_idx] += 1.0 / (k + rank)
    return sorted(scores.keys(), key=lambda d: (-scores[d], d))


# ---------------------------------------------------------------------------
# Evaluation metrics
# ---------------------------------------------------------------------------


def evaluate(queries, chunks, bm25_idx, vec_idx, doc_to_chunks):
    """Evaluate BM25, Vector, and Hybrid modes. Return per-mode metrics."""
    modes = {
        'bm25': {'hits_10': 0, 'mrr_sum': 0.0},
        'vector': {'hits_10': 0, 'mrr_sum': 0.0},
        'hybrid': {'hits_10': 0, 'mrr_sum': 0.0},
    }
    total = len(queries)
    if total == 0:
        return {m: {'recall_10': 0.0, 'mrr': 0.0} for m in modes}

    for q in queries:
        target = doc_to_chunks.get(q['doc_id'], set())

        bm25_results = bm25_idx.search(q['query'], top_k=TOP_K)
        vec_results = vec_idx.search(q['query'], top_k=TOP_K)
        hybrid_results = reciprocal_rank_fusion([bm25_results, vec_results])[:TOP_K]

        for mode_name, results in [('bm25', bm25_results), ('vector', vec_results), ('hybrid', hybrid_results)]:
            # Recall@10
            if set(results[:10]) & target:
                modes[mode_name]['hits_10'] += 1
            # MRR
            for rank, idx in enumerate(results, 1):
                if idx in target:
                    modes[mode_name]['mrr_sum'] += 1.0 / rank
                    break

    return {m: {'recall_10': v['hits_10'] / total, 'mrr': v['mrr_sum'] / total} for m, v in modes.items()}


# ---------------------------------------------------------------------------
# Chunker wrappers (try/except — skip if not installed)
# ---------------------------------------------------------------------------

CHUNKERS = {}


def register_chunker(name):
    def decorator(func):
        CHUNKERS[name] = func
        return func

    return decorator


@register_chunker('LangChain')
def chunk_langchain(docs):
    from langchain_text_splitters import RecursiveCharacterTextSplitter

    splitter = RecursiveCharacterTextSplitter(chunk_size=512, chunk_overlap=50)
    chunks = []
    for doc in docs:
        for text in splitter.split_text(doc['content']):
            chunks.append({'text': text, 'doc_id': doc['id']})
    return chunks


@register_chunker('Chonkie')
def chunk_chonkie(docs):
    from chonkie import TokenChunker

    chunker = TokenChunker(chunk_size=512, chunk_overlap=50)
    chunks = []
    for doc in docs:
        for c in chunker.chunk(doc['content']):
            chunks.append({'text': c.text, 'doc_id': doc['id']})
    return chunks


@register_chunker('Haystack')
def chunk_haystack(docs):
    from haystack import Document
    from haystack.components.preprocessors import DocumentSplitter

    splitter = DocumentSplitter(split_by='word', split_length=100, split_overlap=10)
    hs_docs = [Document(content=doc['content'], meta={'doc_id': doc['id']}) for doc in docs]
    result = splitter.run(documents=hs_docs)
    chunks = []
    for d in result['documents']:
        chunks.append({'text': d.content, 'doc_id': d.meta.get('doc_id', 0)})
    return chunks


# ---------------------------------------------------------------------------
# Benchmark a single framework
# ---------------------------------------------------------------------------


def benchmark_framework(name, chunker_func, docs, queries):
    """Run hybrid search benchmark for one framework."""
    gc.collect()

    mem_before = get_mem_mb()
    t0 = time.perf_counter()

    # Chunk
    try:
        chunks = chunker_func(docs)
    except Exception as e:
        return {'name': name, 'error': str(e)}

    t_chunk = time.perf_counter() - t0

    if not chunks:
        return {'name': name, 'error': 'no chunks produced'}

    # Build doc_id -> chunk indices mapping
    doc_to_chunks = defaultdict(set)
    for i, c in enumerate(chunks):
        doc_to_chunks[c['doc_id']].add(i)

    # Build BM25 index
    t0 = time.perf_counter()
    bm25_idx = BM25Index()
    bm25_idx.build(chunks)
    t_bm25 = time.perf_counter() - t0

    # Build TF-IDF vector index
    t0 = time.perf_counter()
    vec_idx = VectorIndex()
    vec_idx.build(chunks)
    t_vector = time.perf_counter() - t0

    # Evaluate all modes
    t0 = time.perf_counter()
    metrics = evaluate(queries, chunks, bm25_idx, vec_idx, doc_to_chunks)
    t_eval = time.perf_counter() - t0

    mem_after = get_mem_mb()
    total_chars = sum(len(d['content']) for d in docs)

    return {
        'name': name,
        'chunks': len(chunks),
        'chunk_time': t_chunk,
        'bm25_index_time': t_bm25,
        'vector_index_time': t_vector,
        'eval_time': t_eval,
        'total_time': t_chunk + t_bm25 + t_vector + t_eval,
        'mem_delta_mb': mem_after - mem_before,
        'bm25_recall_10': metrics['bm25']['recall_10'],
        'bm25_mrr': metrics['bm25']['mrr'],
        'vector_recall_10': metrics['vector']['recall_10'],
        'vector_mrr': metrics['vector']['mrr'],
        'hybrid_recall_10': metrics['hybrid']['recall_10'],
        'hybrid_mrr': metrics['hybrid']['mrr'],
        'chars': total_chars,
        'docs': len(docs),
        'index_terms': len(bm25_idx.idf),
    }


# ---------------------------------------------------------------------------
# Main pipeline
# ---------------------------------------------------------------------------


def run(root_dir):
    """Run hybrid search benchmark across all frameworks.

    Returns dict compatible with run_comparison.py.
    """
    mem_start = get_mem_mb()

    print('=' * 80)
    print('HYBRID SEARCH BENCHMARK — BM25 + TF-IDF Vector + RRF')
    print(f'Dataset: {root_dir}')
    print('=' * 80)

    docs = load_docs(root_dir)
    if not docs:
        print('No documents found.')
        return {
            'tool': 'hybrid_search',
            'total_time': 0,
            'docs': 0,
            'chars': 0,
            'chunks': 0,
            'index_terms': 0,
            'mem_delta_mb': 0,
        }

    total_chars = sum(len(d['content']) for d in docs)
    print(f'\n{len(docs)} docs, {total_chars:,} chars')

    queries = generate_queries(docs)
    print(f'{len(queries)} queries generated\n')

    results = []
    for name, func in CHUNKERS.items():
        print(f'  Running {name}...', end=' ', flush=True)
        try:
            r = benchmark_framework(name, func, docs, queries)
        except Exception as e:
            print(f'SKIP ({e})')
            continue

        if 'error' in r:
            print(f'SKIP ({r["error"][:60]})')
        else:
            print(f'{r["chunk_time"]:.3f}s, BM25 R@10={r["bm25_recall_10"]:.2f}, Vec R@10={r["vector_recall_10"]:.2f}, Hybrid R@10={r["hybrid_recall_10"]:.2f}')
            results.append(r)

    if not results:
        print('\nNo frameworks completed.')
        mem_final = get_mem_mb()
        return {
            'tool': 'hybrid_search',
            'total_time': 0,
            'docs': len(docs),
            'chars': total_chars,
            'chunks': 0,
            'index_terms': 0,
            'mem_delta_mb': mem_final - mem_start,
        }

    # Comparison table
    print(f'\n{"=" * 80}')
    print('HYBRID SEARCH COMPARISON')
    print(f'{"=" * 80}')
    print(f'{"Framework":<15} | {"BM25 R@10":>10} | {"Vector R@10":>12} | {"Hybrid R@10":>12} | {"Hybrid MRR":>11}')
    print(f'{"-" * 15}-+-{"-" * 10}-+-{"-" * 12}-+-{"-" * 12}-+-{"-" * 11}')
    for r in sorted(results, key=lambda x: -x['hybrid_recall_10']):
        print(f'{r["name"]:<15} | {r["bm25_recall_10"]:>10.2f} | {r["vector_recall_10"]:>12.2f} | {r["hybrid_recall_10"]:>12.2f} | {r["hybrid_mrr"]:>11.2f}')

    # Detailed timing
    print(f'\n{"=" * 80}')
    print('TIMING BREAKDOWN')
    print(f'{"=" * 80}')
    print(f'{"Framework":<15} | {"Chunk":>8} | {"BM25 Idx":>9} | {"Vec Idx":>8} | {"Eval":>8} | {"Total":>8} | {"Mem MB":>8}')
    print(f'{"-" * 15}-+-{"-" * 8}-+-{"-" * 9}-+-{"-" * 8}-+-{"-" * 8}-+-{"-" * 8}-+-{"-" * 8}')
    for r in results:
        print(f'{r["name"]:<15} | {r["chunk_time"]:>8.3f} | {r["bm25_index_time"]:>9.3f} | {r["vector_index_time"]:>8.3f} | {r["eval_time"]:>8.3f} | {r["total_time"]:>8.3f} | {r["mem_delta_mb"]:>8.1f}')

    print(f'\n{"=" * 80}')

    # Return first result as representative for run_comparison.py compatibility
    best = max(results, key=lambda x: x['hybrid_recall_10'])
    mem_final = get_mem_mb()
    return {
        'tool': 'hybrid_search',
        'total_time': best['total_time'],
        'docs': best['docs'],
        'chars': best['chars'],
        'chunks': best['chunks'],
        'index_terms': best['index_terms'],
        'mem_delta_mb': mem_final - mem_start,
        'bm25_recall_10': best['bm25_recall_10'],
        'vector_recall_10': best['vector_recall_10'],
        'hybrid_recall_10': best['hybrid_recall_10'],
        'hybrid_mrr': best['hybrid_mrr'],
        'frameworks': results,
    }


if __name__ == '__main__':
    if len(sys.argv) < 2:
        print(f'Usage: {sys.argv[0]} <docs_directory>')
        sys.exit(1)

    root = sys.argv[1]
    if not os.path.isdir(root):
        print(f'ERROR: {root} not found')
        sys.exit(1)
    run(root)
